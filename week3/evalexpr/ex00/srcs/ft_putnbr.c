#include "eval_expr.h"

void	ft_putnbr(int nb)
{
	int		y;
	long	x;

	x = nb;
	if (x < 0)
	{
		ft_putchar('-');
		x = -x;
	}
	if (x > 9)
		ft_putnbr(x / 10);
	y = (x % 10) + 48;
	ft_putchar(y);
}
